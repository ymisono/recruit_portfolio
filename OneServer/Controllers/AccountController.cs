        // GET api/Account/LoginUserInfo
        [HostAuthentication(DefaultAuthenticationTypes.ExternalBearer)]
        [Route("LoginUserInfo")]
        public async Task<UserInfoViewModel> GetLoginUserInfo()
        {
            ExternalLoginData externalLogin = ExternalLoginData.FromIdentity(User.Identity as ClaimsIdentity);
            
            var curUser = await UserManager.FindByIdAsync(User.Identity.GetUserId());

            return new UserInfoViewModel
            {
                Id = User.Identity.GetUserId(),
                UserName = User.Identity.GetUserName(),
                Email = curUser.Email,
                HasRegistered = externalLogin == null,
                LoginProvider = externalLogin != null ? externalLogin.LoginProvider : null
            };
        }

        // GET api/Account/UserInfo
        //!!危険AdminRoleのみ
        [Route("UserInfo")]
        public IHttpActionResult GetUserInfo()
        {
            var db = new ApplicationDbContext();

            //削除フラグも含めて取ってくる
            var users = db.Users;

            var returnUsers = new List<UserInfo>();

            foreach(var user in users)
            {
                //対応するロールをゲット
                var roles = UserManager.GetRoles(user.Id);
                ICollection<Role> castedRoles = new Collection<Role>();
                foreach(var r in roles)
                {
                    var tempRole = RoleManager.FindByName(r);
                    castedRoles.Add(new Role { Id = tempRole.Id, Name = r, Description = tempRole.Description });
                }

                returnUsers.Add(
                    new UserInfo 
                    {
                        Id = user.Id, UserName = user.UserName,
                                   Email = user.Email,
                                   PhoneNumber = user.PhoneNumber,
                                   Roles = castedRoles,
                                   IsDeleted = user.IsDeleted
                    });
            }

            return Ok(returnUsers);
        }

        //PUT api/Account/UserInfo/{guid}
        [Route("UserInfo")]
        [HttpPut]
        public IHttpActionResult PutUserInfo(UserInfo user)
        {
            //更新
            var db = new ApplicationDbContext();

            var existingUser = db.Users.SingleOrDefault(x => x.Id == user.Id);
            if(existingUser == null)
            {
                return BadRequest();
            }

            existingUser.Email = user.Email;
            existingUser.PhoneNumber = user.PhoneNumber;

            //Roles
            //削除
            foreach (var r in existingUser.Roles)
            {
                var ur = user.Roles.SingleOrDefault(x => x.Id == r.RoleId);
                //なければ
                if (ur == null)
                {
                    //自分自身から消す
                    var removeRole = RoleManager.FindById(r.RoleId);
                    UserManager.RemoveFromRole(r.UserId,removeRole.Name);
                }
            }

            //追加
            foreach(var r in user.Roles)
            {
                var er = existingUser.Roles.SingleOrDefault(x => x.RoleId == r.Id);
                if (er == null)
                {
                    existingUser.Roles.Add(
                        new IdentityUserRole { RoleId = r.Id, UserId = user.Id}
                        );
                }
                
            }

            db.SaveChanges();

            return Ok();
        }

